    public function render()
    {
        // Check if the search input is empty
        // If empty, show only the first 5 icons from the entire icons array.
        // If search input is provided, filter the icons array by the search term,
        // then slice the result to show only the first 5 matching icons.
        $filteredIcons = empty($this->search)
            ? array_slice($this->icons, 0, 5) // Show the first 5 icons if search is empty
            : array_slice(array_filter($this->icons, function ($icon) {
                // Filter icons by checking if the search term exists in the icon name (case-insensitive)
                return stripos($icon, $this->search) !== false;
            }), 0, 5); // Slice the result to show only the first 5 filtered icons

        // Return the view and pass the filtered icons (either the default 5 or the filtered ones)
        return view('livewire.icon-selector', ['showedIcons' => $filteredIcons]);
    }

    public function selectIcon($icon)
    {
        // Set the selected icon based on the user's click
        $this->selectedIcon = $icon;

        // Dispatch a custom browser event 'icon-selected' with the selected icon's value.
        // This allows other Livewire components or JavaScript to listen for the selected icon.
        $this->dispatch('icon-selected', selectedIcon: $this->selectedIcon);
    }

